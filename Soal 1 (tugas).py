#menghitung luas persegi panjang
print("\nMenghitung luas persegi panjang\n")
p = float(input("masukkan nilai panjang: "))
l = float(input("masukan nilai lebar: "))
L = p * l
print("\nluas persegi panjang =\n" ,L)


#menghitung luas lingkaran
print("\nMenghitung luas lingkaran\n")
r = float(input("masukkan nilai jari-jari: "))
L_lingkaran = 3.14 * r * r
print("\nLuas lingkaran =\n" ,L_lingkaran)

#menghitung luas kubus
print("\nMenghitung luas permukaan kubus\n")
s = float(input("masukkan panjang sisi: "))
L_kubus = 6 * s * s
print("\nLuas permukaan kubus =\n" ,L_kubus)

#mengkonversi suhu celcius ke fahrenheit
print("\nMengkonversi suhu celcius ke fahrenheit\n")
c = float(input("masukkan nilai celcius: "))
F = (c * 9/5) + 32
print("\nC =\n" ,F)

#mengkonversi suhu reamur ke kelvin
print("\nMengkonversi suhu reamur ke kelvin\n")
R = float(input("masukkan nilai reamur: "))
K = (5 / 4 * R) + 273
print("K =" ,K)


